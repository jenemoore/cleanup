<h4 align="left"> Summer Reading Program for  Adults, Teens &amp; Youth<br>
  &ldquo;Read to the Rhythm!&rdquo;
  <br4>
  June 1 - July 31, 2015</h4>
<p>The Summer  Reading Program for adults, teens and youth runs through July 31 at the Warrenville  Public Library. Participants of all ages are eligible for great prizes. If the information below does not answer your questions, please call the Information Desk at 630/393-1171 x106 or x110. <a href="#readingrockets">Click here for &quot;Reading Rockets&quot; tip sheets for parents</a>. </p>
<p>&nbsp;</p>
<p><strong>Adult Summer Reading Program</strong><br>
  <strong>Ages 18 and up</strong><br>
  <strong>Last day to  return book logs is July 31.<br>
  </strong>Participants must read or listen to five books from the  Library between June 1 and July 31 and submit their reading log to receive a  free gift. Warrenville Library cardholders also earn a prize drawing entry for  each completed log. Completion of additional reading logs earns added entries  in the prize drawing, but no other free gifts. Prizes in the drawing include a  mini Bluetooth speaker and a $75 Target gift card. Pick up your reading log at the Information Desk or <a href="AdultReadingLogSummer2015.pdf">click here to print a reading log</a>.</p>
<p>&nbsp;</p>
<p> Summer Readers  are also eligible to participate in the monthly Reader Recommendations prize  drawing. Entrants must submit a <a href="2015 Reader Recomm Reviews.pdf">reader recommendation</a> for a book from the Library&rsquo;s  collection. More than one recommendation may be completed. The winners of each  monthly drawing will receive a $20 Target gift card. </p>
<p>&nbsp;</p>
<p><strong>Teen Summer Reading Program</strong><br>
  <strong>Ages 12 &ndash; 17</strong><br>
  <strong>Last day to return book logs is July 31.</strong><br>
</p>
<p>Teen participants must read or listen to five books  between June 1 and July 31 and submit their reading logs to receive a free  gift. Warrenville Library cardholders also earn a prize drawing entry for each  completed log. Completion of additional reading logs earns added entries in the  prize drawing, but no other free gifts. Prizes in the drawing include Beats  headphones, a $30 iTunes gift card and a $30 Starbucks gift card.  Pick up your reading log at the Information Desk or <a href="TeenReadingLogSummer2015.pdf">click here to print a reading log</a>.</p>
<p>&nbsp;</p>
<p><strong>Youth Summer Reading Program</strong><br>
  <strong>Ages Birth &ndash; 11</strong><br>
  <strong>Last day to return book logs is July 31.</strong><br>
</p>
<p>Children must complete six hours of reading between June  1 and July 31 and submit their reading logs to receive a bag of goodies.  Completion of additional reading logs earns more prizes while supplies last.  Warrenville Library cardholders also earn a prize drawing entry for each  completed log. Prizes in the drawing include a Kindle Fire HD, a $75 Main Event  gift card and two $50 Target gift cards.  Pick up your reading log at the Information Desk or <a href="Childrens Log 2015.pdf">click here to print a reading log (English)</a> / <a href="Childrens Log Espanol 2015.pdf">(Spanish)</a>.</p>

