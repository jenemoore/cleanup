<a name="hoopla"><h4>Hoopla</h4></a>

<a href="https://www.hoopladigital.com"><img src="../images/hooplamosaic-02.png" alt="Hoopla Digital" height="50" align="texttop"></a>
<p>Warrenville card holders can download the free Hoopla Digital mobile app on their Android or IOS device or visit <a href="https://www.hoopladigital.com">hoopladigital.com</a> to begin enjoying thousands of titles  from major Hollywood studios, record companies and publishers. The collection includes videos, music, eBooks, eAudiobooks and eComics. Titles are available to borrow 24/7, for instant streaming or temporary downloading to  smartphones, tablets and computers. <!--Each patron may check out up to five items per month.--><strong>During our emergency closure, each member may check out up to ten items per month.</strong> Lending periods for hoopla materials are as follows:
	</p><p>&nbsp;</p>
<ul>
		<li>eBooks, eAudiobooks, eComics: 21 days</li>
		<li>Music: 7 days</li>
		<li>Video: 3 days</li>
	</ul>
</p>

<h4>Instructions</h4>
<p><strong>PC</strong></p>
<ul>
	<li>Visit <a href="https://www.hoopladigital.com">hoopladigital.com</a></li>
	<li>Click Log In in the upper right corner of the screen. Click Sign Up Now at bottom of page.</li>
	<li>On the screen that appears, click Warrenville Public Library. If the library's name does not appear, click Choose a library, locate Warrenville Public Library in the list, and click to select it.</li>
	<li>Create a Hoopla account using your preferred email address and a password of your choice. Your password must be at least 8 characters long. Type your email address and password twice. Click Continue.</li>
	<li>Type your Warrenville Public Library card number (without spaces) and your PIN. By default, your PIN is the four-digit month and day of your birthday.</li>
	<li>Click Sign Up Now.</li>
	<li>Click  Search our Titles to begin looking for titles. You may also browse by format or genre.</li>
	<li><strong>Note: </strong>Hoopla supports Google Chrome, Mozilla Firefox, Microsoft Edge, and Safari. If you are having trouble playing content in your web browser, please update your browser to the newest version.</li>
</ul>

<p><strong>Mobile app</strong></p>
<p>Visit the <a href="https://www.hoopladigital.com/support">hoopla website</a> for a list of supported devices.</p>
<ul>
	<li>Visit Google Play, the iTunes App Store, or the Kindle App Store and sign in. Do a search for the Hoopla Digital app. </li>
	<li>Click "GET," download the free Hoopla Digital app, and open the app.</li>
	<li>Create a Hoopla account using your preferred email address and a password of your choice. Your password must be at least 8 characters long. Sign in to the Hoopla Digital app.</li>
	<li>Type your Warrenville Public Library card number (without spaces) and your PIN. By default, your PIN is the four-digit month and day of your birthday.</li>
	<li>Tap the arrow to the right of "library."</li>
	<li>On the Edit Library page that appears, tap Choose Your Library menu, then touch Warrenville Public Library. Touch Save.</li>
	<li>Tap Search to begin browsing for items or browse by format or genre.</li>
</ul>

<p>Questions? Ask at the Reference Desk or call the Library at 630-393-1171 x121. One-hour one-on-one Book a Librarian sessions are available. You can also click on the three blue lines, scroll to the bottom of the menu and click Contact Us to contact hoopla staff directly. </p>

