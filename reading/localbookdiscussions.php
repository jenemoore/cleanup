<h4>Interlibrary Loan for Local Book Discussion Groups</h4>
<p>  <strong>Guidelines &amp; Procedures</strong><br>
  <em>Warrenville  Public Library offers an interlibrary loan service for local book discussion  groups.&nbsp; </em><em>The Library will  request multiple copies of books from other libraries. &nbsp;This service is available to Warrenville  Library Card Holders in good standing. </em><br>
  </p>
<p>&nbsp;</p>
<p><strong>Guidelines:</strong></p>
<ul class="reading_list">
  <li>The books must be at  least one year old, not  current bestsellers and not  on hold at other libraries. If the chosen title is not available,  we will contact you to discuss an alternate title. </li>
 <li>You may  request a maximum of 15 copies of a book; requests are limited to one request  per group, per month.</li>
 <li>To ensure  availability, submit your requests at least 6 weeks before the book discussion.  You may provide a list of books up to six months in advance. </li></ul>

<p><strong>Procedures:</strong> <br>
  <ul class="reading_list">
  <li> Designate a  leader for your book discussion group, who will contact the Library with the titles  and dates and pick up the items when they become available.  <em>Due to interlibrary loan rules, the  group&rsquo;s leader must be a Warrenville Public Library cardholder. </em></li>
  <li> <a href="Book Discussion Request Form.pdf">Click here to print out a form</a> or stop by  the Circulation Desk. Submit the form to the Circulation Desk at least 6 weeks before your  book discussion meeting. </li>
  <li> The group leader will be notified by phone or email  when the books are available for pick up at the Circulation Desk. The  group leader is responsible for returning the books to the Warrenville Library  by their due date and accepts responsibility for any overdue fines and damaged  or lost item charges. Interlibrary  loan overdue charges are 25&cent; per day per item with no grace period. Items are  not renewable. </li>
  </ul>

<p>If you have  further questions please contact Patty Dybala at 630-393-1171 ext. 102. </p>
