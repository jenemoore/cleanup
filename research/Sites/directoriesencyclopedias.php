<h3>Research &gt; Online Resources  &gt; Internet Directories &amp; Encyclopedias</h3>


<p>&nbsp;</p>
<ul class="resource_list">
<li><a href="https://www.cia.gov/library/publications/the-world-factbook/"><strong>CIA World  Factbook</strong></a> &ndash; Provides national-level information on countries, territories, and  dependencies. Includes flags of the world and reference maps.</li>

 
<li><a href="https://www.encyclopedia.chicagohistory.org/"><strong>Encyclopedia of  Chicago</strong></a> &ndash; Includes  thousands of articles, photos and maps related to the history of Chicago&rsquo;s  industries, people, culture, sports, surrounding cities and towns, politics and  more. </li>
 
<li><a href="https://www.encyclopedia.com"><strong>Encyclopedia.com</strong></a> &ndash; Comprehensive online encyclopedia  and database of over 51,000 entries from over 100 encyclopedias and  dictionaries. </li>
 
<li><a href="https://www.excite.com/"><strong>Excite</strong></a> &ndash; Personalization Web portal, featuring subject listings,  search, content and functionality. </li>
 
<li><a href="https://www.infoplease.com"><strong>Information Please </strong></a> &ndash; A mega-reference site that  includes facts on thousands of subjects. Includes almanacs, dictionary,  encyclopedia, atlas thesaurus, and homework center.</li>

<li><a href="https://www.archive.org"><strong>The Internet Archive</strong></a> &ndash; is a non-profit digital library  which offers permanent storage and access to collections of digitized  materials, including websites, music, moving images, and books.</li>

<li><a href="https://www.imdb.com"><strong>Internet Movie  Database</strong></a> &ndash; News  source for information on all aspects of film &ndash; actors, directors, etc.</li>

<li><a href="https://www.ipl.org/"><strong>ipl2</strong></a>  &ndash; Searchable and annotated subject directory of Internet resources selected and  evaluated by librarians. </li>
  
<li><a href="https://www.itc.nl/Pub/Home/library/Library-general-information/more-info-databases/lii_info.html"><strong>Librarians Internet  Index</strong></a> &ndash; Publicly funded, &ldquo;You can search  and browse the website for the best of the Web. There are over 20,000 entries,  maintained by librarians and organized into 14 main topics and nearly 300  related topics.&rdquo;</li>

<li><a href="https://libraryspot.com"><strong>Library Spot</strong></a> &ndash; Library and reference resources,  including top dictionaries, encyclopedias, newspapers, maps, quotations, and  more.</li>

<li><a href="https://www.almanac.com"><strong>Old Farmer's Almanac</strong></a> &ndash; Info on the weather, gardening,  astronomy, food, and the outdoors.</li>
 
<li><a href="http://www.onlineconversion.com"><strong>OnlineConversion.com</strong></a> &ndash; A collection of interactive sites  that estimate, evaluate, calculate, and translate.</li>

<li><a href="https://www.refdesk.com"><strong>RefDesk.com</strong></a> &ndash; A myriad of news, weather,  sports, reference, encyclopedias, leading news stories, and more.</li>

 
<li><a href="https://www.quotationspage.com/"><strong>The Quotations Page</strong></a> &ndash; Over 26,000 quotations online from  over 3,100 authors, and more are added daily. Includes links to other  quotations sites.</li>

<li><a href="https://www.wikipedia.org/"><strong>Wikipedia</strong></a> &ndash; Free encyclopedia that anyone can edit, with convenient  links between topics, includes bibliographies.</li>
 
</ul>
