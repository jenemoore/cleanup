<ul class="resource_list">
<li><a href="https://chicagohs.org/research"><strong>Chicago History Museum Resources</strong></a> &ndash; Provides  links to the museum&rsquo;s online catalog and special historical project sites such  as <em>The History Files</em>, which highlights  the 9 most memorable events in Chicago history.</li>

<li><A href="https://rpa.warrenville.com/rpa/default/webauth.htm?rs=nbCTrib" target="_blank"><strong>Chicago Tribune Historical Archives</strong></A> &ndash; Full-text of historical issues and events from the 1920s to 1984, and obituaries from 1860 to 1984. </li>

<li><a href="https://dp.la/"><strong>Digital Public Library of America</strong></a> &ndash; The Digital Public Library of America brings together the riches of America's libraries, archives, and museums, and makes them freely available to the world.</li>

<li><a href="https://www.dupagehistory.org"><strong>DuPage County Historical Society</strong></a> &ndash; The DuPage County  Historical Society promotes and preserves the history of DuPage County through  publications, tours, lectures, and cooperative efforts with other DuPage County  historical organizations.</li>

<li><a href="https://www.encyclopedia.chicagohistory.org/"><strong>Encyclopedia of Chicago</strong></a> &ndash; Includes thousands of  articles, photos and maps related to the history of Chicago&rsquo;s industries,  people, culture, sports, surrounding cities and towns, politics and more. This  site is produced by a collaboration of the Chicago Historical Society, the  Newberry Library, and Northwestern University.</li>

<li><a href="https://www.historychannel.com"><strong>The History Channel</strong> &ndash; </a>At The History Channel  you will find historical information ranging from Great Speeches (in audio) to  facts about This Day in History.</li>

<li><a href="https://www.historyillinois.org/"><strong>Illinois State Historical Society</strong></a> &ndash; Illinois State Historical Society provides links to all aspects of Illinois history, government,  geography, and culture for both adults and children.</li>

<li><a href="https://www.loc.gov/collections/"><strong>Library of Congress Digital Collections</strong></a> &ndash; Browse dozens of digital collections featuring written and spoken words,  sound recordings, still and moving images, prints, maps, and sheet music.</li>

<li><a href="https://www.archives.gov"><strong>National Archives and Records Administration</strong></a> &ndash; The United States  National Archives and Records Administration (NARA) is an independent agency of  the United States federal government charged with preserving and documenting  government and historical records. It also works to increase public access to  those documents.</li>

<li><a href="https://www.nwhm.org/"><strong>National  Women&rsquo;s History Museum</strong></a> &ndash; Information on outstanding women in history.</li>


<li><a href="https://www.taxhistory.org/"><strong>Tax History Project</strong></a> &ndash; Public service initiative from Tax Analysts. Established in 1995,  the Project provides scholars, policymakers, journalists, and the general  public with information on the history of U.S. public finance.</li>

<li><a href="https://www.warrenvillehistorical.org/"><strong>Warrenville Historical Society</strong></a> &ndash; This organization  operates the Warrenville Historical Museum, which collects, preserves and  displays objects and documents that illustrate Warrenville's history, and  brings together individuals interested in that history. </li>
</ul>

