<h3>Research &gt; Online Resources  &gt; Genealogy</h3>


<p>&nbsp;</p>
<p>A number of helpful <strong>guides  to online genealogy searching</strong> can be found in the Library&rsquo;s general and  reference collections. The general collection guides are shelved with call number <strong>929.10285</strong>.</p>
<p>&nbsp;</p>
<ul class="resource_list">
<li><A href="https://rpa.warrenville.com/rpa/default/webauth.htm?rs=nbObit" target="_blank"><strong>Americas Obituary and Death  Notices</strong></a> &ndash; The  largest and most comprehensive collection of newspaper obituaries and death  notices. Search by name, date range, or text. <strong>This  database is available  remotely with your Warrenville Library card number and 4 digit PIN. </strong></li>

<li><a href="https://ancestrylibrary.proquest.com/" target="_blank"><strong>Ancestry  Library Edition</strong></a> &ndash; Includes records from the U.S. census (1790);  military record; court, land and probate records; vital and church records;  directories and more. <strong>This resource not available for remote access  - may only be accessed at the library.</strong></li>

<li><a href="https://www.deathindexes.com/illinois/cook.html"><strong>Chicago Death Records</strong></a> &ndash; A Genealogy Guide for Finding Death  Certificates, Obituaries &amp; Other Records.</li>

<li><a href="https://www.chicagogenealogy.org/"><strong>Chicago Genealogical Society</strong></a> &ndash; Includes &ldquo;A potpourri of links for  Chicago and Cook County genealogy; Illinois genealogy; Archives / Libraries /  Societies; and General genealogy.&rdquo;</li>

<li><a href="https://archives.chicagotribune.com/"><strong>Chicago  Tribune Historical Archive</strong></a> &ndash; The Chicago Tribune covers international  and nation news, as well as important issues and events in Chicago, Illinois  and the entire Midwest. (1857-1991)</li>

<li><a href="https://www.cyndislist.com/huguenot.htm"><strong>Cyndi's List of Genealogy Sites on  the Internet</strong></a> &ndash; Over 35,000 categorized and cross-referenced links for genealogy  sites.</li>

<li><a href="https://www.dcgs.org"><strong>DuPage  Genealogical Society</strong></a> &ndash; Contains info and links on cemeteries, funeral homes, online  publications, online queries, and more.</li>

<li><a href="https://www.familysearch.org"><strong>FamilySearch</strong></a> &ndash; The Church of Jesus  Christ of Latter-day Saints' Family History Library provides an online search of the millions of names  in its International Genealogical Index, and of genealogical sites.</li>

<li><a href="https://findagrave.com"><strong>Find A Grave</strong></a> &ndash; Lists locations and inscriptions of more than 100 million grave records worldwide.

<li><a href="https://www.thoughtco.com/genealogy-4133308"><strong>Genealogy at ThoughtCo.</strong></a> &ndash; Offers tips and strategies for researching genealogies and family histories.

<li><a href="https://rpa.warrenville.com/rpa/default/webauth.htm?rs=HeritageQ" target="_blank"><strong>Heritage Quest Online</strong></a> &ndash; Resource for census data, family records and local histories. <strong>This  database is available  remotely with your Warrenville Library card number and 4 digit PIN. </strong></li>

<li><a href="https://www.legacy.com/NS/"><strong>Legacy.com</strong></a> &ndash; Legacy.com offers access to more  than 78 million records from the Social Security Death Index dating back to  1937, and hosts more than 10 million obituaries from the majority of prominent  newspapers nationwide.</li>

<li><a href="https://www.archives.gov"><strong>National Archives and Records Administration</strong></a> &ndash; Holds the records of  the United States Federal government. Online access to photos, census data,  U.S. legal manuscripts, the Federal Register, and numerous additional  materials.</li>

<li><a href="https://stevemorse.org/"><strong>A One-Step Portal for On-Line Genealogy</strong></a> &ndash; Sophisticated search tools by genealogist Stephen P. Morse which may provide better access to some immigration and census records, as well as tools for dealing with non-Roman alphabets and variant calendars.

<li><a href="https://searches.rootsweb.com"><strong>Roots Web</strong></a> &ndash; Connects people so  that they can help each other and share genealogical research. An award winning  genealogical resource with searchable databases.</li>

<li><a href="https://www.vitalrec.com"><strong>U.S. Vital Records Information</strong></a> &ndash; Info about where to  obtain vital records (i.e. birth or death certificate, marriage license, and  divorce decrees) from each state, territory, and county of the U.S. </li>
</ul>
