<h4>History</h4>
<p>In the 1950s, the library was housed in 1,000 square feet in the Community   Building and was essentially a volunteer library filled with donated materials.   Ten years later, the library closed due to a lack of volunteers and funding. The   Warrenville Public Library District was formed by a referendum held in February   1979 with the citizens approving a 15-cent rate for library services. In 1986,   voters approved another 15-cent tax rate increase to build and operate a 10,000   square foot facility on Stafford Place. In September of 2003, a large addition   and renovation project which tripled the size of the facility was completed. The   building was made possible by the City of Warrenville TIF funds. </p>
<p>&nbsp;</p>

<p>The Warrenville Public Library District is a member of the <a href="https://www.railslibraries.info/">Reaching Across   Illinois Library System (RAILS)</a>.</p>

<H4>Mission </H4>
<p>It is the mission of the Warrenville Public Library District to collect,   organize and make available the representative records of humanity&rsquo;s actions,   concerns and aspirations. It exists for the common good to support a literate   and informed citizenry.</p>




