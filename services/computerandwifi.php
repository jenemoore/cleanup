<p><strong>Can library staff assist me?</strong><br>
Other than confirming that the wireless  network is accessible, library staff are unable to provide technical assistance.  You must be familiar with your wireless device and know how to set it up for  wireless access.</p>
<p>&nbsp;</p>

<p><strong>Is the wireless network secure? </strong><br>
The network is unfiltered and unsecured. It  is strongly recommended that you not use this network to transmit personal,  financial, or legal data. Users are responsible for protecting their hardware  from outside threats such as viruses and spam. The Library assumes no  responsibility or liability for any damages or loss resulting from use of the  wireless network.</p>
<p>&nbsp;</p>
  
<p><strong>What if I don&rsquo;t have a wireless device?</strong><br>
The Library has 20 public access computers  that enable you to access the Internet along with a suite of Microsoft Office  products. You must have a library card and PIN as well as a signed User  Agreement form on file to use these computers. </p>
<p>&nbsp;</p>

<p><strong>How fast is the connection? </strong><br>
Connection speed will vary depending upon  many factors including, but not limited to the number of users and your  distance from the wireless access point. The Library reserves the right to  limit bandwidth to ensure fair sharing of network resources. </p>
<p>&nbsp;</p>

<p><strong>What about power?</strong><br>
We realize that you may need to plug your  device into an outlet. You may use any unused outlet as long as you do not  obstruct any pathways or disturb any other equipment. If you remove a child  safety outlet plug, please replace it when you are finished.</p>
<p>&nbsp;</p>

<p><strong>Where are the hot spots?</strong><br>
The wireless network is accessible from  anywhere on the Library&rsquo;s main level, including the meeting and study rooms,  and in the caf&eacute; area on the lower level. The wireless network may also be  available from outside the Library building; however signal strength and  reliability are not monitored or guaranteed.</p>


