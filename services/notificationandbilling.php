		<li>7 days after due date...........First overdue notice</li>
		<li>14 days after due date..........Account blocked, privileges suspended</li>
		<li>21 days after due date..........Second overdue notice</li>
		<li>42 days after due date..........Bill</li>
	</ul>
	
<h4>Billing</h4>
	<p>A bill will be sent for the following:
		<ul>
			<li>Items 42 days overdue</li>
			<li>Unpaid lost or damaged items</li>
		</ul>
